# Linear transformation multiplicands (the LFSR feedback coefficients).
# l(a) = a[0]*148 + a[1]*32 + a[2]*133 + a[3]*16 + a[4]*194 + a[5]*192 + a[6]*1
#      + a[7]*251 + a[8]*1 + a[9]*192 + a[10]*194 + a[11]*16 + a[12]*133
#      + a[13]*32 + a[14]*148 + a[15]*1
# in GF(2^8) with reduction polynomial x^8 + x^7 + x^6 + x + 1 (0xC3 mod, but mod is 0x1C3).
COEFS = [148, 32, 133, 16, 194, 192, 1, 251, 1, 192, 194, 16, 133, 32, 148, 1]


def gf_mul(a, b, poly=0x1C3):
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        carry = a & 0x80
        a = (a << 1) & 0xFF
        if carry:
            a ^= poly & 0xFF  # XOR with low 8 bits of poly (x^8 reduces by xoring 0xC3)
        b >>= 1
    return p & 0xFF


def main():
    from pathlib import Path
    rom_dir = Path(__file__).resolve().parent / "roms"
    roms = []
    for i in range(16):
        f = rom_dir / f"M_{i:05d}.txt"
        d = {}
        for line in f.read_text().splitlines():
            idx, val = line.split("\t")
            d[int(idx)] = int(val, 16)
        roms.append([d[i] for i in range(256)])

    # Try matching ROM[i][x] == COEFS[i] * Pi[x] for various orderings.
    print("Test: ROM[i][x] == COEFS[i] * Pi[x] in GF(2^8) ?")
    for i in range(16):
        c = COEFS[i]
        match = True
        for x in range(256):
            if roms[i][x] != gf_mul(c, PI[x]):
                match = False
                break
        print(f"  ROM[{i:2}], coef={c:3} (0x{c:02x}): match={match}, ROM[{i}][0]={roms[i][0]:02x}, expected {gf_mul(c,PI[0]):02x}")


if __name__ == "__main__":
    main()
