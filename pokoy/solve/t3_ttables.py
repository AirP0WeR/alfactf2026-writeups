def xtime(b):
    return ((b << 1) ^ (0x1b if b & 0x80 else 0)) & 0xff

def gmul(a, b):
    r = 0
    for i in range(8):
        if b & 1:
            r ^= a
        a = xtime(a)
        b >>= 1
    return r & 0xff

def build_T():
    T = [[0]*256 for _ in range(4)]
    for x in range(256):
        s = AES_SBOX[x]
        m2 = gmul(s, 2)
        m3 = gmul(s, 3)
        T[0][x] = (m2 << 24) | (s << 16) | (s << 8) | m3
        T[1][x] = (m3 << 24) | (m2 << 16) | (s << 8) | s
        T[2][x] = (s << 24) | (m3 << 16) | (m2 << 8) | s
        T[3][x] = (s << 24) | (s << 16) | (m3 << 8) | m2
    return T

T_AES = build_T()

# For each ttable, find (which T0..T3, k_in, mask) such that
# ttable[x] = T_b[AES_SBOX[x] (?)... no — input itself is the byte, ttable[x] = T_b[x XOR k_in] XOR mask
# Actually T_r,b in T-table AES: T_r,b[x] = MixColumns column from SBOX(x XOR k_r,b).
# So ttable[x] = T_b[x XOR k_in] XOR mask.

results = []
for ti, t in enumerate(ttables):
    found_for_t = []
    for tb in range(4):
        for k_in in range(256):
            mask = t[0] ^ T_AES[tb][0 ^ k_in]
            ok = True
            for x in range(256):
                if t[x] != (T_AES[tb][x ^ k_in] ^ mask):
                    ok = False
                    break
            if ok:
                found_for_t.append((tb, k_in, mask))
    results.append(found_for_t)

# stats
